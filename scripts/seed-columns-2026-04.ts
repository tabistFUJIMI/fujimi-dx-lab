async function main() {
  const force = process.argv.includes("--force");
  console.log(`[seed] 対象: ${ARTICLES.length}件`);

  for (const article of ARTICLES) {
    const existing = await prisma.column.findUnique({ where: { slug: article.slug } });
    if (existing && !force) {
      console.log(`[skip] ${article.slug} (既存・--forceで上書き可)`);
      continue;
    }

    const data = {
      slug: article.slug,
      title: article.title,
      excerpt: article.excerpt,
      content: article.content,
      category: article.category,
      tags: article.tags,
      ogImage: article.ogImage,
      isPublished: true,
      publishedAt: new Date(),
    };

    if (existing) {
      await prisma.column.update({ where: { slug: article.slug }, data });
      console.log(`[update] ${article.slug}`);
    } else {
      await prisma.column.create({ data });
      console.log(`[create] ${article.slug}`);
    }
  }

  console.log("[seed] 完了");
}

